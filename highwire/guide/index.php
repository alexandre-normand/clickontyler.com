    <h1>Highwire Guide</h1>
    <p>So, what is Highwire? It's a little complicated to explain at first, so bear with me. I'm still working on how best to introduce it to folks...</p>
    <p>In a nutshell, Highwire is a competitor to Apple's "Back to My Mac" service. You run one copy on your Mac at home, and another on your Mac at work (or at a coffee shop, etc). Highwire will then connect back to itself on your home computer and let you access all of its shared services.</p>
    <p>Lost yet? What does this really mean?</p>
    <p>Well, it means that when you're at work you'll be able to listen to your iTunes music stored on your Mac at home. Or browse your iPhoto library at home. Or access any of your shared folders and files. It all works pretty seamlessly. You just open up Highwire on each Mac, and instantly they'll be able to share back and forth.</p>
    <p>Make sense yet?</p>
    <p>You can <a href="http://clickontyler.com/highwire/download/">download the latest version of Highwire here</a>.</p>
    
    <h2>Getting Started</h2>
    <p>1. The first thing you'll want to do after launching Highwire is to create yourself a Highwire account. This is just a simple online account that coordinates all of the data between copies of Highwire. <strong>Click the Register button to sign up.</strong></p>
    <p><img src="1.png"></p>

    <p>2. Once you've signed in to Highwire, you're given two options. You can either <strong>Share</strong> or <strong>Connect</strong>. It works like this: First, you <strong>share</strong> your Mac at home. Then, when you're at work or at a coffee shop, you <strong>connect</strong> back to your home machine.</p>
    <p><img src="2.png"></p>
    
    <h2>Sharing Your Home Mac</h2>
    <p>To share your home Mac, just click on the <strong>Turn on Sharing</strong> button. It's that easy. Highwire should prompt you if it encounters any errors.</p>
    <p>Note: In order for Highwire to work, you'll need to have <strong><a href="3.png">Remote Login</a></strong> (also known as SSH) enabled on your Mac.</p>
    
    <h2>Connecting Back to your Home Mac</h2>
    <p>Connecting back to your Home Mac is easy. Simply select your Mac from the list of available computers and click <strong>Connect</strong>. When prompted, enter your Mac's username and password. If all goes well, Highwire should be able to connect back to your Mac and begin sharing your services.</p>
    <p><img src="4.png"></p>
    
    <h2>How Do I Know if it Worked?</h2>
    <p>Good question. The easiest way to test is using iTunes.</p>
    <ol>
        <li>On your Mac at <strong>home</strong>, start iTunes and <a href="5.png">make sure that network sharing is turned on</a>.</li>
        <li>On your Mac at <strong>home</strong>, launch Highwire and begin sharing.</li>
        <li>On your other Mac at <strong>work or wherever</strong>, launch Highwire and connect back to your Home mac.</li>
        <li>Once connected, if you open iTunes on your <strong>work Mac</strong>, you should <a href="6.png">see your <strong>home</strong> music library shared and playable</a>.</li>
        <li>Tada! You're done!</li>
    </ol>
    
    <h2>So What Else Can Highwire Do?</h2>
    <p>The technical geek answer is <em>Highwire will share all of your Bonjour services over the internet</em>. In other words, your iTunes Music Library, your iPhoto Photo Library, your shared files and folders, etc. Any Bonjour service can be made to work with Highwire.</p>
    

